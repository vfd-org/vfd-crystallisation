print(f"  arrow test Ω(f) signs over 8 random f: {[int(s) for s in signs]}  -> {'consistent' if len(set(signs))==1 else 'NO consistent sign (orientation gives nothing)'}")

# ---- Stage D: contraction representation Q_W = H^{1/2}(I-K)H^{1/2}, ||K||<=1 (route_b) ----
w,Uv=np.linalg.eigh(H); Hm=Uv@np.diag(1/np.sqrt(w))@Uv.T; Hh2=Uv@np.diag(np.sqrt(w))@Uv.T
K=Hm@Rm@Hm; K=(K+K.T)/2; mu=np.linalg.eigvalsh(K).max()
