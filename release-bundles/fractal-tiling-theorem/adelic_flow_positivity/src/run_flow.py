# ---- A,P,R components of the centre Q_W=A+P-R (route_b build) ----
def components(NC=12,S=3.0,P=2000):
    CENT=[14.0+4.0*k for k in range(NC)]; Wd=S/math.sqrt(2); RG=np.arange(-150,150,0.1)
    M=np.array([float(mp.re(mp.digamma(mp.mpc(0.25,r/2)))) for r in RG])
    lam={}; comp=[False]*(P+1)
    for p in range(2,P+1):
        if not comp[p]:
            for m in range(p*p,P+1,p): comp[m]=True
            pk=p
            while pk<=P: lam[pk]=math.log(p); pk*=p
    nid=np.array(sorted(lam)); logn=np.log(nid); lw=np.array([lam[int(n)] for n in nid])/np.sqrt(nid)
    def terms(ci,cj): return [(math.exp(-(a-b)**2/(4*S**2)),(a+b)/2) for a in (ci,-ci) for b in (cj,-cj)]
    A=np.zeros((NC,NC));Pm=np.zeros((NC,NC));Rm=np.zeros((NC,NC))
    for i in range(NC):
        for j in range(NC):
            t=terms(CENT[i],CENT[j]); Hg=np.zeros_like(RG)
            for a,m in t: Hg+=a*np.exp(-(RG-m)**2/(S**2))
            A[i,j]=float(np.sum(Hg*M)*0.1/(2*math.pi))
            pole=2*sum(a*np.exp(-((1j*0.5)-m)**2/(S**2)) for a,m in t); g0=sum(a*(Wd/math.sqrt(2*math.pi)) for a,m in t)
            Pm[i,j]=float(np.real(pole-g0*math.log(math.pi)))
            pr=0j
            for a,m in t: pr+=2*np.sum(lw*(a*(Wd/math.sqrt(2*math.pi))*np.exp(-Wd**2*logn**2/2)*np.exp(-1j*m*logn)))
            Rm[i,j]=float(np.real(pr))
    sym=lambda X:(X+X.T)/2; return sym(A),sym(Pm),sym(Rm)
A,Pm,Rm=components(); H=A+Pm; Qw=A+Pm-Rm
print(f"\n[centre] Q_W=A+P−R min eig={np.linalg.eigvalsh(Qw).min():+.5f} (PSD,near-null); H=A+P min eig={np.linalg.eigvalsh(H).min():+.4f} (capacity PSD)")

# ---- Stage B/F: directed scale flow is UNITARY -> cannot create positivity ----
u,du=W.witness_grid(U=8,N=256); D=W.scale_generator(256,du)
