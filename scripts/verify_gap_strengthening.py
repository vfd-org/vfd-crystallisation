    # SR1: irreducibility of V_k|2I for k<=5 (rung Thm 3.2 hypothesis).
    # |2I|=120; the irreducible characters' norms: <chi_Vk, chi_Vk> = 1 iff
    # irreducible. Use the SU(2) character on the 9 conjugacy classes of 2I.
    # 2I conjugacy classes (angle, size): standard.
    classes = [(0.0, 1), (np.pi, 1), (np.pi / 5, 12), (2 * np.pi / 5, 12),
               (3 * np.pi / 5, 12), (4 * np.pi / 5, 12), (np.pi / 3, 20),
               (2 * np.pi / 3, 20), (np.pi / 2, 30)]
    sizes = np.array([c[1] for c in classes])
    assert sizes.sum() == 120

    def chi(k, th):
        return np.sin((k + 1) * th) / np.sin(th) if abs(np.sin(th)) > 1e-12 \
            else (k + 1) * np.cos((k + 1) * th) / np.cos(th)
    irr = []
    for k in range(6):
        ch = np.array([chi(k, th) for th, _ in classes])
        norm = (sizes * ch * ch).sum() / 120
        irr.append(abs(norm - 1) < 1e-9)
    check("SR1 irreducibility of V_k|2I for k=0..5 (rung Thm 3.2 / rendering "
          "Thm 2.3 hypothesis): all <chi,chi>=1", all(irr),
          f"k where irreducible: {[k for k in range(6) if irr[k]]}")
