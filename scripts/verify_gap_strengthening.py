    N = len(v)
    # a 2D chart patch: take a vertex and two independent neighbour directions,
    # build a 2D grid of anchors via the (commuting) right-translations is hard
    # on the discrete set; instead test the one-form integrability directly.
    # arg-min field for quadratic Delta_cl(Pi;u) = 1/2|Pi|^2 - <Pi, g(u)>:
    #   argmin = g(u). Conservative iff g = grad phi. Test discrete curl of g
    #   over plaquettes of a 2D coordinate grid.
    n = 80
    xs = np.linspace(-1, 1, n)
    X, Y = np.meshgrid(xs, xs)
    dx = xs[1] - xs[0]
    # CONSERVATIVE field g = grad(phi), phi = sin(x)cos(y): curl -> 0 as dx->0
    gx = np.cos(X) * np.cos(Y); gy = -np.sin(X) * np.sin(Y)
    curl_c = ((gy[:-1, 1:] - gy[:-1, :-1]) - (gx[1:, :-1] - gx[:-1, :-1])) / dx
    # NON-INTEGRABLE field g = (-y, x): curl identically 2 (rotation)
    gx2 = -Y; gy2 = X
    curl_n = ((gy2[:-1, 1:] - gy2[:-1, :-1]) - (gx2[1:, :-1] - gx2[:-1, :-1])) / dx
    check("S1a conservative arg-min one-form (g = grad phi): area-normalised "
          "circulation -> 0 (integrable; O(dx^2) discretisation error only)",
          np.abs(curl_c).max() < 0.05 * np.abs(curl_n).mean(),
          f"conservative {np.abs(curl_c).max():.2e} << non-integ "
          f"{np.abs(curl_n).mean():.2f}")
    check("S1b GENUINE NULL: a non-integrable arg-min field (g=(-y,x)) has "
          "circulation = 2 -> NO consistent scene (the test CAN fail)",
          abs(np.abs(curl_n).mean() - 2.0) < 1e-6,
          f"circulation/area = {np.abs(curl_n).mean():.4f} (theory 2)")
