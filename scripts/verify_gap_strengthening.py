def qmul(a, b):
    w0, x0, y0, z0 = a; w1, x1, y1, z1 = b
    return np.array([w0*w1 - x0*x1 - y0*y1 - z0*z1,
                     w0*x1 + x0*w1 + y0*z1 - z0*y1,
                     w0*y1 - x0*z1 + y0*w1 + z0*x1,
                     w0*z1 + x0*y1 - y0*x1 + z0*w1])


def qconj(q):
    return np.array([q[0], -q[1], -q[2], -q[3]])


